


function brick(x,y)
{
    var brick ={};

    brick.x = x;
    brick.y = y;

    brick.w = 20;
    brick.h = 20;
    

    brick.brick_drawing = function()
    {
        
        ctx.fillStyle = "black";
        ctx.fillRect(  brick.x,  brick.y, brick.w, brick.h);
    }

    return brick;
}

function road(x,y)
{
    var road ={};
    road.x = x;
    road.y = y;

    road.w = 20;
    road.h = 20;

    road.road_drawing = function()
    {
        
        ctx.fillStyle = "white";
        ctx.fillRect(  road.x,  road.y, road.w, road.h);
    }

    return road;
}


